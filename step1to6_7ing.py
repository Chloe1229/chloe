def show_step7():
    st.header("Step 7. 판단조건에 따른 필요서류 및 보고유형 도출")

    step6_result = st.session_state.get('step6_result', {})

    results_to_show = {}

    for item in step7_decision_table:
        title = item['title']
        sub_item = item['sub_item']
        condition_numbers = item['condition_numbers']
        condition_states = item['condition_states']

        if title not in step6_result:
            continue

        title_data = step6_result.get(title, {})

        if sub_item:
            selected_subitem = title_data.get('선택사항', {}).get(sub_item, None)
            if selected_subitem is None:
                continue
        else:
            selected_subitem = None

        condition_matched = True
        for cond_num, cond_state in zip(condition_numbers, condition_states):
            user_choice = title_data.get('충족요건', {}).get(str(cond_num), None)
            if user_choice != cond_state:
                condition_matched = False
                break

        if condition_matched:
            results_to_show[title] = {
                'name_lines': item['name_lines'],
                'result_docs': item['result_docs'],
                'result_report_type': item['result_report_type']
            }

    for title_key, result in results_to_show.items():
        st.markdown("---")
        st.markdown("### [제조방법의 변경사항]")
        for line in result['name_lines']:
            st.write(line)
        st.markdown("")
        st.markdown("### [필요서류]")
        st.write("필요서류는 다음과 같습니다.")
        for doc in result['result_docs']:
            st.write(doc)
        st.markdown("")
        st.markdown("### [보고유형]")
        st.write(f"보고유형은 다음과 같습니다.\n**{result['result_report_type']}**")

    if st.button("이전단계로"):
        st.session_state['page'] = 6
        st.rerun()
    if st.button("다음단계로"):
        st.session_state['page'] = 8
        st.rerun()

if __name__ == "__main__":
    if 'page' not in st.session_state:
        st.session_state['page'] = 1
    if st.session_state['page'] == 6:
        show_step6()
    elif st.session_state['page'] == 7:
        show_step7()
    else:
        main()
